# Generating Random Integers
import random
# Mathematical Functions
import math
a=10
b=3

print(abs(-10))  # Absolute value (10)
print(pow(a, b))  # Power function (10^3 = 1000)
print(math.sqrt(16))  # Square root (4.0)
print(math.factorial(5))  # Factorial (5! = 120)
print(math.gcd(10, 15))  # Greatest common divisor (5)

# Checking Properties of Integers
print(a % 2 == 0)  # Check if even (True)
print(b % 2 != 0)  # Check if odd (True)



print(random.randint(1, 100))  # Random number between 1 and 100
